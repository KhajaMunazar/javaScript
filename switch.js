 let day = 5;
 switch (day) {
  case 1:
  console.log("Monday");
  break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 3:
    console.log("Thursday");
    break;
  case 4:
    console.log("Friday");
    break;
  case 5:
    console.log("Saturday");
    break;
  case 6:
    console.log("Sunday");
    break;
  default:
    console.log("Another day");
 }