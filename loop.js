//Loops: Iterating Elements

//There are 3 types of loops
// for loop
// while loop
// do while loop

// for loop
for (i = 0;i <= 10;i++) {
  console.log(i)
}

//while loop
 
let k = 1;

while (k <= 10) {
  console.log(k)
  k++;
}

//do while loop

let a = 0;
do {
  console.log(a)
  a++
} while (a <= 10);